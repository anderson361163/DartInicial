void main(){
  int contador =0;
  while(contador <= 10){

    print('O valor do contador é $contador');
    contador++;
  }
  print('While finalizado');

  print('---------------');

  contador =0;

  do{
        print("O valor do contador é $contador");
        contador++;
  }while(contador <= 10);
   print('Do While finalizado');
}